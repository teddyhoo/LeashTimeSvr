function nameList($str) {
	foreach(array_unique(explode("\n", $str)) as $line) {
		$parts = explode(' ', trim($line));
		$name = array();
		$name['lname'] = $parts[count($parts)-1];
		$name['fname'] = trim(substr($line, 0, 0-strlen($name['lname'])-1));
		$names[] = $name;
	}
	return $names;	
}

function allNames() {
	global $female, $male;
	$names = array_merge(nameList($male), nameList($female));
	shuffle($names);
	return $names;
}	

