function randomStreet($street) {
	$parts = explode(' ', $street);
	if(!$parts || !is_numeric($parts[0])) return;
	$parts[0] = rand(max(1, (int)$parts[0] - 100), (int)$parts[0] + 100);
	return join(' ', $parts);
}

function noPhones($arr) {
	foreach(array('homephone', 'cellphone', 'workphone') as $k) $arr[$k] = null;
	return $arr;
}